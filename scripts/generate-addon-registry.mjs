import { createHash, createPublicKey, verify } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { isAbsolute, relative, resolve, sep } from "node:path";

const root = process.cwd();
const configPath = resolve(root, process.env.NR_ADDONS_REGISTRY_FILE ?? "addons.registry.json");
const outputPath = resolve(root, ".generated", "addon-registry.ts");
const allowlist = new Map([
  ["webshop", "@nr-cms/webshop/server"],
  ["license-server", "@nr-cms/license-server/server"],
]);
const raw = JSON.parse(await readFile(configPath, "utf8"));
const addons = Array.isArray(raw?.addons) ? raw.addons : [];
const releasePublicKeys = addons.length > 0 ? await readReleasePublicKeys() : {};
const entries = [];
for (const item of addons) {
  if (!item || typeof item !== "object" || typeof item.addonKey !== "string" || typeof item.packageName !== "string") throw new Error("Invalid addon registry entry.");
  const specifier = allowlist.get(item.addonKey);
  if (!specifier || item.packageName !== specifier.replace(/\/server$/, "")) throw new Error(`Unallowlisted addon registry entry: ${item.addonKey}`);
  await verifyInstalledRelease(item, releasePublicKeys);
  entries.push(`  ${JSON.stringify(item.addonKey)}: () => import(${JSON.stringify(specifier)}),`);
}
const output = `/* Generated by scripts/generate-addon-registry.mjs. Do not edit manually. */\nexport const addonLoaders = {\n${entries.join("\n")}\n} as const;\n\nexport type RegisteredAddonKey = keyof typeof addonLoaders;\nexport type AddonLoader = () => Promise<unknown>;\n\nexport function getAddonLoader(addonKey: string): AddonLoader | null {\n  return (addonLoaders[addonKey as RegisteredAddonKey] as AddonLoader | undefined) ?? null;\n}\n`;
await mkdir(resolve(root, ".generated"), { recursive: true });
await writeFile(outputPath, output, "utf8");

async function readReleasePublicKeys() {
  const configuredPath = process.env.NR_ADDON_RELEASE_PUBLIC_KEYS_FILE;
  if (!configuredPath) throw new Error("NR_ADDON_RELEASE_PUBLIC_KEYS_FILE is required for a non-empty addon registry.");
  let parsed;
  try {
    parsed = JSON.parse(await readFile(resolve(configuredPath), "utf8"));
  } catch {
    throw new Error("NR_ADDON_RELEASE_PUBLIC_KEYS_FILE must point to readable JSON.");
  }
  if (!parsed || typeof parsed !== "object" || Array.isArray(parsed)) throw new Error("Addon release public key set must be an object.");
  for (const [kid, pem] of Object.entries(parsed)) {
    if (!/^[A-Za-z0-9._:-]{3,200}$/.test(kid) || typeof pem !== "string" || !pem.includes("BEGIN PUBLIC KEY")) throw new Error("Addon release public key set contains an invalid entry.");
    let key;
    try {
      key = createPublicKey(pem);
    } catch {
      throw new Error("Addon release public key set contains an unreadable key.");
    }
    if (key.asymmetricKeyType !== "ed25519") throw new Error("Addon release authority keys must be Ed25519 public keys.");
  }
  return parsed;
}

async function verifyInstalledRelease(expected, publicKeys) {
  if (
    typeof expected.packageVersion !== "string" ||
    !/^[a-f0-9]{64}$/.test(expected.artifactSha256 ?? "") ||
    typeof expected.signingKid !== "string"
  ) {
    throw new Error(`Registry entry ${expected.addonKey} must pin packageVersion, artifactSha256 and signingKid.`);
  }
  const packageRoot = resolve(root, "node_modules", ...expected.packageName.split("/"));
  const [packageJson, manifest, provenance] = await Promise.all([
    readJson(resolve(packageRoot, "package.json"), `${expected.addonKey} package metadata`),
    readJson(resolve(packageRoot, "release-manifest.json"), `${expected.addonKey} release manifest`),
    readJson(resolve(packageRoot, "provenance.json"), `${expected.addonKey} provenance`),
  ]);
  if (
    packageJson.name !== expected.packageName ||
    packageJson.version !== expected.packageVersion ||
    manifest.manifestVersion !== 1 ||
    manifest.runtimeContractVersion !== "1" ||
    manifest.addonKey !== expected.addonKey ||
    manifest.packageName !== expected.packageName ||
    manifest.packageVersion !== expected.packageVersion ||
    manifest.artifact?.sha256 !== expected.artifactSha256 ||
    provenance.subject?.sha256 !== expected.artifactSha256 ||
    manifest.signingKid !== expected.signingKid
  ) {
    throw new Error(`Installed addon release identity mismatch: ${expected.addonKey}.`);
  }
  if (!Array.isArray(manifest.artifact?.files) || manifest.artifact.files.length === 0) throw new Error(`Installed addon release has no artifact inventory: ${expected.addonKey}.`);
  const aggregateInput = stablePretty(manifest.artifact.files);
  const aggregateSha256 = sha256(Buffer.from(aggregateInput, "utf8"));
  if (
    aggregateSha256 !== manifest.artifact.sha256 ||
    Buffer.byteLength(aggregateInput) !== manifest.artifact.size ||
    (manifest.artifact.registryIntegrity &&
      manifest.artifact.registryIntegrity !==
        `sha256-${Buffer.from(aggregateSha256, "hex").toString("base64")}`)
  ) {
    throw new Error(`Installed addon release aggregate checksum mismatch: ${expected.addonKey}.`);
  }
  if (typeof manifest.signature !== "string" || !/^[A-Za-z0-9_-]{86}$/.test(manifest.signature)) throw new Error(`Installed addon release signature is malformed: ${expected.addonKey}.`);
  const publicKey = publicKeys[manifest.signingKid];
  if (!publicKey) throw new Error(`Installed addon release uses an unpinned signing key: ${expected.addonKey}.`);
  const { signature, ...unsigned } = manifest;
  if (!verify(null, Buffer.from(canonicalJson(unsigned), "utf8"), publicKey, Buffer.from(signature, "base64url"))) throw new Error(`Installed addon release signature verification failed: ${expected.addonKey}.`);

  const inventoryPaths = new Set();
  for (const file of manifest.artifact.files) {
    if (!file || typeof file.path !== "string" || !/^[A-Za-z0-9._/-]+$/.test(file.path) || typeof file.sha256 !== "string" || !/^[a-f0-9]{64}$/.test(file.sha256) || !Number.isSafeInteger(file.size) || file.size < 0) throw new Error(`Installed addon release inventory is invalid: ${expected.addonKey}.`);
    if (inventoryPaths.has(file.path)) throw new Error(`Installed addon release inventory contains duplicate paths: ${expected.addonKey}.`);
    const artifactPath = resolve(packageRoot, file.path);
    if (!isWithin(packageRoot, artifactPath)) throw new Error(`Installed addon release inventory escapes its package: ${expected.addonKey}.`);
    let contents;
    try {
      contents = await readFile(artifactPath);
    } catch {
      throw new Error(`Installed addon release artifact is missing: ${expected.addonKey}.`);
    }
    if (contents.byteLength !== file.size || sha256(contents) !== file.sha256) throw new Error(`Installed addon release artifact checksum mismatch: ${expected.addonKey}.`);
    inventoryPaths.add(file.path.replace(/^\.\//, ""));
  }
  const serverEntrypoint = String(manifest.entrypoints?.server ?? "").replace(/^\.\//, "");
  if (!inventoryPaths.has(serverEntrypoint)) throw new Error(`Installed addon server entrypoint is outside the signed artifact inventory: ${expected.addonKey}.`);
}

async function readJson(path, label) {
  try {
    return JSON.parse(await readFile(path, "utf8"));
  } catch {
    throw new Error(`${label} is missing or invalid.`);
  }
}

function canonicalJson(value) {
  return JSON.stringify(sortValue(value));
}

function stablePretty(value) {
  return JSON.stringify(sortValue(value), null, 2);
}

function sortValue(value) {
  if (Array.isArray(value)) return value.map(sortValue);
  if (!value || typeof value !== "object") return value;
  return Object.fromEntries(
    Object.entries(value)
      .filter(([, entry]) => entry !== undefined)
      .sort(([left], [right]) => left.localeCompare(right))
      .map(([key, entry]) => [key, sortValue(entry)]),
  );
}

function sha256(contents) {
  return createHash("sha256").update(contents).digest("hex");
}

function isWithin(parent, child) {
  const path = relative(parent, child);
  return path !== "" && path !== ".." && !path.startsWith(`..${sep}`) && !isAbsolute(path);
}
