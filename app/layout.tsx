import type { Metadata } from "next";
import { ClerkProvider, Show, SignInButton, SignUpButton } from "@clerk/nextjs";
import { currentUser } from "@clerk/nextjs/server";
import { UserButtonWithRoles } from "@/components/user-button-with-roles";
import { shadcn } from "@clerk/themes";
import { Geist, Geist_Mono } from "next/font/google";
import { Button } from "@/components/ui/button";
import Link from "next/link";
import {
  NavigationMenu,
  NavigationMenuItem,
  NavigationMenuLink,
  NavigationMenuList,
  navigationMenuTriggerStyle,
} from "@/components/ui/navigation-menu";
import { getRoles, hasRole } from "@/lib/roles";
import { SiteTopMenu } from "@/components/site-top-menu";
import "./globals.css";

const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const user = await currentUser();
  const roles = getRoles(user?.publicMetadata);
  const isBackendUser =
    hasRole(roles, "admin") ||
    hasRole(roles, "publisher") ||
    hasRole(roles, "author");
  const isAdmin = hasRole(roles, "admin");
  return (
    <html
      lang="en"
      className={`dark ${geistSans.variable} ${geistMono.variable} h-full antialiased`}
    >
      <body className="min-h-full flex flex-col">
        <ClerkProvider appearance={{ theme: shadcn }}>
          <header
            className="sticky top-0 z-50 bg-background flex items-center justify-between p-4 gap-4 h-16"
            style={{
              borderBottom: "1px solid #349aee",
              boxShadow: "0 1px 16px 2px #349aee88, 0 2px 32px 4px #349aee33",
            }}
          >
            <a
              href="/"
              className="text-xl font-bold tracking-tight text-gray-400 hover:text-foreground transition-colors"
            >
              Night Raven CMS
            </a>
            <div className="flex items-center gap-4">
              <SiteTopMenu />
              {isBackendUser && (
                <NavigationMenu>
                  <NavigationMenuList>
                    <NavigationMenuItem>
                      <NavigationMenuLink
                        asChild
                        className={navigationMenuTriggerStyle()}
                      >
                        <Link href="/dashboard">Dashboard</Link>
                      </NavigationMenuLink>
                    </NavigationMenuItem>
                    <NavigationMenuItem>
                      <NavigationMenuLink
                        asChild
                        className={navigationMenuTriggerStyle()}
                      >
                        <Link href="/dashboard/content">Content</Link>
                      </NavigationMenuLink>
                    </NavigationMenuItem>
                    {isAdmin && (
                      <>
                        <NavigationMenuItem>
                          <NavigationMenuLink
                            asChild
                            className={navigationMenuTriggerStyle()}
                          >
                            <Link href="/dashboard/users">Users</Link>
                          </NavigationMenuLink>
                        </NavigationMenuItem>
                        <NavigationMenuItem>
                          <NavigationMenuLink
                            asChild
                            className={navigationMenuTriggerStyle()}
                          >
                            <Link href="/dashboard/content-categories">
                              Content Categories
                            </Link>
                          </NavigationMenuLink>
                        </NavigationMenuItem>
                        <NavigationMenuItem>
                          <NavigationMenuLink
                            asChild
                            className={navigationMenuTriggerStyle()}
                          >
                            <Link href="/dashboard/top-menu">Top Menu</Link>
                          </NavigationMenuLink>
                        </NavigationMenuItem>
                      </>
                    )}
                  </NavigationMenuList>
                </NavigationMenu>
              )}
              <Show when="signed-out">
                <SignInButton mode="modal">
                  <Button variant="ghost" size="lg" className="cursor-pointer">
                    Sign in
                  </Button>
                </SignInButton>
                <SignUpButton mode="modal">
                  <Button
                    variant="outline"
                    size="lg"
                    className="cursor-pointer"
                  >
                    Sign up
                  </Button>
                </SignUpButton>
              </Show>
              <Show when="signed-in">
                <UserButtonWithRoles />
              </Show>
            </div>
          </header>
          {children}
          <footer
            className="sticky bottom-0 z-50 bg-background mt-auto px-6 py-8 text-sm text-muted-foreground"
            style={{
              borderTop: "1px solid #349aee",
              boxShadow: "0 -1px 16px 2px #349aee88, 0 -2px 32px 4px #349aee33",
            }}
          >
            <div className="mx-auto flex max-w-5xl flex-col gap-6 sm:flex-row sm:items-start sm:justify-between">
              <div className="flex flex-col gap-1">
                <span className="font-semibold text-foreground">Contact</span>
                <a
                  href="mailto:radomir.radojevic@gmail.com"
                  className="underline hover:text-foreground"
                >
                  radomir.radojevic@gmail.com
                </a>
              </div>
              <div className="sm:text-right">
                <span>
                  &copy; {new Date().getFullYear()} Night Raven CMS. All rights
                  reserved.
                </span>
              </div>
            </div>
          </footer>
        </ClerkProvider>
      </body>
    </html>
  );
}
