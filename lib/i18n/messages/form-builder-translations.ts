function createTranslationMap(row: PhraseMap): Record<string, string> {
  return Object.fromEntries(
    Object.entries(PHRASES).map(([key, source]) => [
      source,
      row[key as PhraseKey],
    ]),
  );
}

function createExtraTranslationMap(
  row: ExtraPhraseMap,
): Record<string, string> {
  return Object.fromEntries(
    Object.entries(EXTRA_PHRASES).map(([key, source]) => [
      source,
      row[key as ExtraPhraseKey],
    ]),
  );
}

export const FORM_BUILDER_SOURCE_TRANSLATIONS = Object.fromEntries(
  Object.entries(TEXT).map(([language, row]) => [
    language,
    {
      ...createTranslationMap(row),
      ...createExtraTranslationMap(EXTRA_TEXT[language as LocalizedLanguage]),
    },
  ]),
) as Record<LocalizedLanguage, Record<string, string>>;
