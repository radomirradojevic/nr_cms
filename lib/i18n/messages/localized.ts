function isRecord(value: unknown): value is Record<string, unknown> {
  return typeof value === "object" && value !== null && !Array.isArray(value);
}

function isPluralMessages(value: unknown): value is PluralMessages {
  if (!isRecord(value)) return false;
  const entries = Object.entries(value);

  return (
    entries.length > 0 &&
    entries.every(
      ([key, message]) =>
        PLURAL_CATEGORIES.has(key) && typeof message === "string",
    )
  );
}

function shouldPreserveSource(source: string): boolean {
  return PRESERVED_SOURCE_STRINGS.has(source) || source.trim().length === 0;
}

function localizeString(
  source: string,
  language: LocalizedLanguage,
  profile: LanguageProfile,
): string {
  const sourceTranslations: Record<string, string> =
    SOURCE_TRANSLATION_PATCHES[language];
  const coreUiTranslations: Record<string, string> =
    CORE_UI_SOURCE_TRANSLATIONS[language];
  const addonShellTranslations: Record<string, string> =
    ADDON_SHELL_SOURCE_TRANSLATIONS[language];
  const lockUiTranslations: Record<string, string> =
    LOCK_UI_SOURCE_TRANSLATIONS[language];
  const dashboardRootTranslations: Record<string, string> =
    DASHBOARD_ROOT_SOURCE_TRANSLATIONS[language];
  const commentsTranslations: Record<string, string> =
    DASHBOARD_COMMENTS_SOURCE_TRANSLATIONS[language];
  const translated =
    coreUiTranslations[source] ??
    addonShellTranslations[source] ??
    lockUiTranslations[source] ??
    commentsTranslations[source] ??
    dashboardRootTranslations[source] ??
    sourceTranslations[source] ??
    profile.translations[source];
  if (translated) return translated;
  if (shouldPreserveSource(source)) return source;

  return source;
}

export function localizeSourceString(
  source: string,
  language: LocalizedLanguage,
): string {
  return localizeString(source, language, LANGUAGE_PROFILES[language]);
}

function pluralCategoriesForLanguage(
  language: LocalizedLanguage,
): readonly PluralCategory[] {
  if (language === "ar") {
    return ["zero", "one", "two", "few", "many", "other"];
  }

  if (SLAVIC_PLURAL_LANGUAGES.has(language)) {
    return ["one", "few", "many", "other"];
  }

  return ["one", "other"];
}

function localizePluralMessages(
  source: PluralMessages,
  language: LocalizedLanguage,
  profile: LanguageProfile,
): PluralMessages {
  const localized: Partial<Record<PluralCategory, string>> = {};
  const fallbackSource = source.other ?? source.one ?? "";

  for (const category of pluralCategoriesForLanguage(language)) {
    localized[category] = localizeString(
      source[category] ?? fallbackSource,
      language,
      profile,
    );
  }

  return localized;
}

function localizeNode(
  source: unknown,
  language: LocalizedLanguage,
  profile: LanguageProfile,
): unknown {
  if (typeof source === "string") {
    return localizeString(source, language, profile);
  }

  if (isPluralMessages(source)) {
    return localizePluralMessages(source, language, profile);
  }

  if (!isRecord(source)) {
    return source;
  }

  return Object.fromEntries(
    Object.entries(source).map(([key, value]) => [
      key,
      localizeNode(value, language, profile),
    ]),
  );
}

export function createLocalizedMessages(language: LocalizedLanguage): Messages {
  return localizeNode(en, language, LANGUAGE_PROFILES[language]) as Messages;
}
