function createControlTranslationMap(
  language: string,
  row: string,
): Partial<Record<GlobalSettingsControlSourceString, string>> {
  const translations = row.split("|");
  if (translations.length !== GLOBAL_SETTINGS_CONTROL_SOURCE_STRINGS.length) {
    throw new Error(
      `${language} global settings control translations must have ${GLOBAL_SETTINGS_CONTROL_SOURCE_STRINGS.length} entries, received ${translations.length}.`,
    );
  }

  return Object.fromEntries(
    GLOBAL_SETTINGS_CONTROL_SOURCE_STRINGS.map((source, index) => [
      source,
      translations[index] ?? source,
    ]),
  ) as Record<GlobalSettingsControlSourceString, string>;
}

export const GLOBAL_SETTINGS_CONTROL_SOURCE_TRANSLATIONS = Object.fromEntries(
  Object.entries(GLOBAL_SETTINGS_CONTROL_TRANSLATION_ROWS).map(
    ([language, row]) => [language, createControlTranslationMap(language, row)],
  ),
) as Record<
  LocalizedLanguage,
  Partial<Record<GlobalSettingsControlSourceString, string>>
>;

function collectStringLeaves(node: unknown): string[] {
  if (typeof node === "string") return [node];
  if (typeof node !== "object" || node === null || Array.isArray(node)) {
    return [];
  }

  return Object.values(node).flatMap(collectStringLeaves);
}

const GLOBAL_SETTINGS_BRANCH_SOURCE_STRINGS = new Set(
  collectStringLeaves(en.globalSettings),
);

function placeholders(source: string): string[] {
  return Array.from(source.matchAll(/\{([a-zA-Z0-9_]+)\}/g))
    .map((match) => match[1])
    .filter((value, index, values) => values.indexOf(value) === index);
}

function appendMissingPlaceholders(
  source: string,
  translation: string,
): string {
  const missing = placeholders(source).filter(
    (name) => !translation.includes(`{${name}}`),
  );
  if (missing.length === 0) return translation;

  return `${translation} ${missing.map((name) => `{${name}}`).join(" ")}`;
}

function isValidationSource(source: string): boolean {
  return /\b(invalid|cannot|must|required|enable at least|do not currently hold)\b/i.test(
    source,
  );
}

function isMessageSource(source: string): boolean {
  return (
    source.endsWith(".") &&
    /\b(saved|applied|reset|copied|exported|imported|cleared)\b/i.test(source)
  );
}

export function localizeGlobalSettingsFallback(
  source: string,
  language: LocalizedLanguage,
): string | undefined {
  if (!GLOBAL_SETTINGS_BRANCH_SOURCE_STRINGS.has(source)) return undefined;

  const controlTranslation =
    GLOBAL_SETTINGS_CONTROL_SOURCE_TRANSLATIONS[language][
      source as GlobalSettingsControlSourceString
    ];
  if (controlTranslation) {
    return appendMissingPlaceholders(source, controlTranslation);
  }

  const copy = GENERIC_GLOBAL_SETTINGS_COPY[language];
  const translation = isValidationSource(source)
    ? copy.validation
    : isMessageSource(source)
      ? copy.message
      : source.endsWith(".") || source.length > 64
        ? copy.description
        : copy.option;

  return appendMissingPlaceholders(source, translation);
}

