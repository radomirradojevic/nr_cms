function createTranslationMap(
  language: string,
  translations: readonly string[],
): Record<GlobalSettingsSourceString, string> {
  if (translations.length !== GLOBAL_SETTINGS_SOURCE_STRINGS.length) {
    throw new Error(
      `${language} global settings translations must have ${GLOBAL_SETTINGS_SOURCE_STRINGS.length} entries.`,
    );
  }

  return Object.fromEntries(
    GLOBAL_SETTINGS_SOURCE_STRINGS.map((source, index) => [
      source,
      translations[index] ?? source,
    ]),
  ) as Record<GlobalSettingsSourceString, string>;
}

export const GLOBAL_SETTINGS_SOURCE_TRANSLATIONS = Object.fromEntries(
  Object.entries(GLOBAL_SETTINGS_TRANSLATION_ROWS).map(
    ([language, translations]) => [
      language,
      createTranslationMap(language, translations),
    ],
  ),
) as Record<LocalizedLanguage, Record<GlobalSettingsSourceString, string>>;
