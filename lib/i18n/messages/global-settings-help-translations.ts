function createMap(
  language: string,
  translations: readonly string[],
): Record<HelpSource, string> {
  if (translations.length !== GLOBAL_SETTINGS_HELP_SOURCE_STRINGS.length) {
    throw new Error(
      `${language} global settings help translations must have ${GLOBAL_SETTINGS_HELP_SOURCE_STRINGS.length} entries, received ${translations.length}.`,
    );
  }

  return Object.fromEntries(
    GLOBAL_SETTINGS_HELP_SOURCE_STRINGS.map((source, index) => [
      source,
      translations[index] ?? source,
    ]),
  ) as Record<HelpSource, string>;
}

export const GLOBAL_SETTINGS_HELP_SOURCE_TRANSLATIONS = Object.fromEntries(
  Object.entries(ROWS).map(([language, translations]) => [
    language,
    createMap(language, translations),
  ]),
) as Record<LocalizedLanguage, Record<HelpSource, string>>;
